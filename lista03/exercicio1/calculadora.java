public class calculadora {
    
    public static int sum(int num1, int num2){
        return num1 + num2;    
    }
    public static int sub(int num1, int num2){
        return num1 - num2;
    }
    public static int div(int num1, int num2){
        return num1/num2;
    }
    public static int times(int num1, int num2){
        return num1*num2;
    }
}
